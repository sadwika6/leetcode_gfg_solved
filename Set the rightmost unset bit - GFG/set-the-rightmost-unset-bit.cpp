class Solution
{
public:
    int setBit(int N)
    {
        // Write Your Code here
        int c=log2(N)+1;
        for(int i=0;i<c;i++)
        {
            if(((N>>i)&1) == 0)
            {
                N=(N|(1<<i));
                break;
            }
        }
        return N;
    }
};

